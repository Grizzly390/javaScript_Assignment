//Q. Use the while loop to print numbers from 1 to 25 in ascending and descending order.

//Ascending order:
let numForAscendingOrder=1;
while(numForAscendingOrder<=25){
    console.log(numForAscendingOrder);
    numForAscendingOrder++;
}

//Descending order:
let numForDescendingOrder=25;
while(numForDescendingOrder>=1){
    console.log(numForDescendingOrder);
    numForDescendingOrder--;
}